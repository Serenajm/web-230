/**
 * Params: x, y
 * Response: x + y
 * Description: returns the sum x and y
 */
function add(x, y) {
    return x + y;
  }
  
  // output
  console.log('The sum of 1 and 2 is: ' + add(1, 2));
  
  // or
  const sum = add(2, 2);
  
  // output
  console.log('The sum of 1 and 2 is: ' + sum);
  
  